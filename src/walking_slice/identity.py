# ---------------------------------------------------------------------------
# Canonical-form regex.
#
# Per design §"Cross-Cutting Concerns" (*Identifier generation*) and AD-WS-2.
# - ``[0-9a-f]`` excludes uppercase to enforce the lowercase canonical form.
# - The third group starts with ``7`` (UUID version nibble).
# - The fourth group starts with one of ``[89ab]`` (variant bits 10xx).
# ---------------------------------------------------------------------------


CANONICAL_UUID7_REGEX: Final[re.Pattern[str]] = re.compile(
    r"^[0-9a-f]{8}-[0-9a-f]{4}-7[0-9a-f]{3}-[89ab][0-9a-f]{3}-[0-9a-f]{12}$"
)


# ---------------------------------------------------------------------------
# Identifier kinds accepted by the ``Identifier_Registry.kind`` CHECK
# constraint (see :mod:`walking_slice.persistence`). Centralized here so the
# Python surface fails fast on a typo before reaching the database, and so
# downstream callers can pick from a single authoritative set.
# ---------------------------------------------------------------------------


ALLOWED_IDENTIFIER_KINDS: Final[frozenset[str]] = frozenset(
    {
        "resource",
        "revision",
        "relationship",
        "region",
        "region_occurrence",
        "immutable_record",
        "trail",
        "trail_revision",
        "trail_step",
        "manifest",
    }
)


# ---------------------------------------------------------------------------
# Reason code written to ``Audit_Records.reason_code`` for every denial
# appended by :meth:`IdentityService.reject_if_duplicate`. Pinned by the
# Identifier-conflict path in design §"Error Handling".
# ---------------------------------------------------------------------------


IDENTIFIER_CONFLICT_REASON_CODE: Final[str] = "identifier-conflict"


# ---------------------------------------------------------------------------
# Distinct identifier types.
#
# These are ``NewType`` aliases over :class:`str` so they round-trip through
# JSON/SQL without conversion but remain statically distinguishable from a
# bare ``str``. Pydantic value objects in :mod:`walking_slice.models` accept
# them transparently when coerced to :class:`uuid.UUID`.
# ---------------------------------------------------------------------------


ResourceId = NewType("ResourceId", str)
