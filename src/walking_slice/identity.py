"""Identity_Service — UUIDv7 generation, canonical-form validation, and
Identifier_Registry persistence with audit-aware conflict rejection.

Design references:

- ``.kiro/specs/first-walking-slice/design.md`` §"Identity_Service" — the
  public surface implemented here (every ``new_*`` factory plus
  :meth:`IdentityService.validate_canonical` and
  :meth:`IdentityService.reject_if_duplicate`).
- AD-WS-2 — every managed identity is a UUIDv7 in canonical lowercase
  hyphenated 8-4-4-4-12 hex form, generated by ``Identity_Service``.
- AD-WS-3 — Resource Identity and Resource Revision Identity (and Trail
  Identity vs Trail Revision Identity) are independent values. The
  separate factory methods exposed here let callers obtain each kind of
  identifier without leaking that they all share the UUIDv7 generator.
- AD-WS-5 — when a caller passes a SQLAlchemy connection to
  :meth:`IdentityService.reject_if_duplicate`, the binding row is
  inserted into ``Identifier_Registry`` *inside the caller's transaction*
  so identifier persistence is atomic with the originating write.
- Design §"Error Handling — Identifier conflict (Requirement 1.4)":
  on a conflict :meth:`reject_if_duplicate` appends a Denial Record
  carrying ``reason_code='identifier-conflict'`` in a separate audit-only
  transaction, then raises :class:`IdentityConflictError` so the
  caller's originating transaction rolls back.

Task scope (task 2.2):

This module adds *persistent* registry behavior on top of task 2.1's
in-memory surface. Behaviour is selected by whether the caller passes a
SQLAlchemy connection to :meth:`reject_if_duplicate`:

- No connection — the in-memory ``dict``-backed registry from task 2.1
  is used. This is what unit tests that construct ``IdentityService()``
  with no arguments rely on.
- Connection supplied — the SQL ``Identifier_Registry`` table is the
  source of truth and a row is INSERTed inside the caller's transaction.
  On conflict, the service uses its injected engine + audit log to
  append a Denial Record from a separate transaction.

Requirements satisfied:

- 1.1 — every managed entity receives exactly one UUIDv7 in canonical
        lowercase 8-4-4-4-12 hex form before becoming referenceable
        (factories are unchanged from task 2.1).
- 1.2 — Resource vs Revision (and Trail vs Trail Revision) identities
        are produced by separate factory methods.
- 1.4 — re-binding an identifier to different domain content is rejected
        with :class:`IdentityConflictError`; a Denial Record is appended
        per design §"Error Handling" before the exception propagates.
- 1.6 — once issued and persisted, an identifier row in
        ``Identifier_Registry`` is never re-bound to a different
        content digest. UPDATE/DELETE on the table are rejected by the
        triggers installed in :mod:`walking_slice.persistence`.
- 1.7 — no business meaning is embedded; identifiers come from
        ``uuid_utils.uuid7()`` and the ``kind``/``content_digest``
        columns are written exactly as the caller supplies them.
"""

